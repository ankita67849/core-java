/*
This Program Demonstrate EscapeSequence
EscapeSequence - Special Character used in Java
1. \n - Create a New line
2. \t - Create tab
3. \"Hello!\" How are you?
4. Ram\'s Dog
*/
package com.biswa.fundamental;

public class EscapeSequenceTest {
	public static void main(String[] args) {
		System.out.println("Biswaranjan Mohapatra");
		System.out.println("Age : 35");
		System.out.println("Address : New Delhi");
		System.out.println("----------");
		System.out.println("Biswaranjan Mohapatra \nAge : 35 \nAddress : New Delhi");
		System.out.println("Name \tAddress \tAge");
		
		System.out.println("\"Hello!\" How are you?");
		System.out.println("Ram\'s Dog");
		
		
	}

}
